/*Написати функцію, яка малює квадрат із зірочок,
 розмір квадрата залежить від введених параметрів.*/
 #include <iostream>
using namespace std;

// Функція для малювання квадрата із зірочок
void drawSquare(int size) {
    // Перевіряємо, чи розмір є додатнім
    if (size <= 0) {
        cout << "Розмір квадрата має бути додатнім числом!" << endl;
        return;
    }

    // Виводимо квадрат із зірочок
    for (int i = 0; i < size; i++) {
        for (int j = 0; j < size; j++) {
            cout << "* ";  // Малюємо зірочку з пробілом
        }
        cout << endl;  // Перехід на новий рядок
    }
}

int main() {
    int n;
    cout << "Введіть розмір квадрата: ";
    cin >> n;

    drawSquare(n);  // Викликаємо функцію для малювання квадрата

    return 0;
}
